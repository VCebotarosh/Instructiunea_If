petale=int(input("Dati numarul de petale ale margaretei: "))
if((petale%5)==0):
    print("Nu ma iubeste deloc")
elif((petale%5)==1):
    print("Ma iubeste un pic")
elif((petale%5)==2):
    print("Ma iubeste mult")
elif((petale%5)==3):
    print("Ma iubeste cu pasiune")
elif((petale%5)==4):
    print("Ma iubeste la nebunie")
